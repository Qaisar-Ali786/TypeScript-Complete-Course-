// a literal type is a type that represent the exact value not just a general type like string or number.

let color: "red" | "green" | "blue";
color = "red";
console.log(color);

let isTure: true | false;
isTure = false;
console.log(isTure);

let number: 1 | 2 | 3;
number = 3;
console.log(number);
